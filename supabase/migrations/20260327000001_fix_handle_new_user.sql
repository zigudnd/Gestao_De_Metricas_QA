-- Corrige o search_path do trigger: o GoTrue roda como supabase_auth_admin
-- com search_path=auth, portanto a função deve referenciar public.profiles explicitamente.

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, email, display_name)
  values (
    new.id,
    new.email,
    coalesce(new.raw_user_meta_data->>'display_name', split_part(new.email, '@', 1))
  );
  return new;
end;
$$;
